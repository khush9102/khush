public class DefaultValue
{
	static boolean a;
   	static byte b;
	static String c;
   	static short d;
   	static int e;
   	static long f;
	static float g;
	static double h;
   	
	public static void main(String[] args)
	{
      		System.out.println("Default value of boolean: " + a);
      		System.out.println("Default value of byte: " + b);
      		System.out.println("Default value of char: " + c);
      		System.out.println("Default value of short: " + d);
      		System.out.println("Default value of int: " + e);
      		System.out.println("Default value of long: " + f);
      		System.out.println("Default value of float: " + g);
      		System.out.println("Default value of double: " + h);
   	}
}